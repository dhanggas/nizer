    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        penduduk.setNik(txtNik.getText());
        penduduk.setNama(txtNama.getText());
        penduduk.setNama_ibu(txtNamaIbu.getText());
        penduduk.setNama_ayah(txtNamaAyah.getText());
        penduduk.setTmp_lahir(txtTmpLahir.getText());
        penduduk.setTgl_lahir(Date.valueOf(ValueFormatter.getDateSQL(txtTglLahir.getDate())));
        if (rbLaki.isSelected()) {
            penduduk.setKelamin(rbLaki.getText());
        } else {
            penduduk.setKelamin(rbPerempuan.getText());
        }
        penduduk.setGol_darah(txtGolDarah.getText());
        penduduk.setAlamat(txtAlamat.getText());
        penduduk.setRt(txtRt.getText());
        penduduk.setRw(txtRw.getText());
        penduduk.setKelurahan(txtKelurahan.getText());
        penduduk.setKecamatan(txtKecamatan.getText());
        penduduk.setAgama(txtAgama.getText());
        penduduk.setPendidikan(txtPendidikan.getText());
        penduduk.setSts_kawin(txtStsKawin.getText());
        penduduk.setPekerjaan(txtPekerjaan.getText());
        if (rbWni.isSelected()) {
            penduduk.setKewarganegaraan(rbWni.getText());
        } else {
            penduduk.setKewarganegaraan(rbWna.getText());
        }
        

        if (isUpdate()) {
            try {
                repoPenduduk.update(penduduk);
                this.dispose();
                if (daftarPendudukController != null) {
                    daftarPendudukController.refreshDataTablesByName();
                    daftarPendudukController.setFields(penduduk);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex, getTitle(), JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(DataPendudukView.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                repoPenduduk.save(penduduk);
                if (daftarPendudukController != null) {
                    daftarPendudukController.refreshDataTablesByName();
                    daftarPendudukController.setFields(penduduk);
                    daftarPendudukController.selectLastRow();
                    daftarPendudukController.btnTambahRequesFocus();
                }
                this.dispose();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex, getTitle(), JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(DataPendudukView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnSimpanActionPerformed
