    private void setFields(Penduduk p) {
        txtNik.setText(penduduk.getNik());
        txtNama.setText(penduduk.getNama());
        txtNamaIbu.setText(penduduk.getNama_ibu());
        txtNamaAyah.setText(penduduk.getNama_ayah());
        txtTmpLahir.setText(penduduk.getTmp_lahir());
        txtTglLahir.setDate(penduduk.getTgl_lahir());
        if (p.getKelamin().equals(rbLaki.getText())) {
            rbLaki.setSelected(true);
        } else {
            rbPerempuan.setSelected(true);
        }
        txtGolDarah.setText(penduduk.getGol_darah());
        txtAlamat.setText(penduduk.getAlamat());
        txtRt.setText(penduduk.getRt());
        txtRw.setText(penduduk.getRw());
        txtKelurahan.setText(penduduk.getKelurahan());
        txtKecamatan.setText(penduduk.getKecamatan());
        txtAgama.setText(penduduk.getAgama());
        txtPendidikan.setText(penduduk.getPendidikan());
        txtStsKawin.setText(penduduk.getSts_kawin());
        txtPekerjaan.setText(penduduk.getPekerjaan());
        if (p.getKewarganegaraan().equals(rbWni.getText())) {
            rbWni.setSelected(true);
        } else {
            rbWna.setSelected(true);
        }
        
    }

